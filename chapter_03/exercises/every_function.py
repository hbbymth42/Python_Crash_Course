languages_list = ['English', 'French', 'Spanish', 'Mandarin', 'Japanese']
print(f"Original Languages List: {languages_list}")
print(f"Number of Languages: {len(languages_list)}")
print(f"Sorted Languages List: {sorted(languages_list)}")
languages_list.reverse()
print(f"Reverse Sorted Languages List (Alter Original): {languages_list}")
languages_list.sort()
print(f"Sorted Languages List (Alter Original): {languages_list}")